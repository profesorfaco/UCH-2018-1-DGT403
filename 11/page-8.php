             <h1>Lean UX Canvas</h1>

<p class="lead">Canvas es una herramienta que permite recoger toda la información relevante sobre un tema en un solo lugar para guiar el proceso de desarrollo de un servicio o producto.
     El lean UX canvas es una herramienta que ayuda a los equipos a trabajar siguiendo la metodología de Lean UX. Esta metodología es una variante del diseño centrado en las personas que se basa en el proceso de construir-medir-aprender
     El objetivo de esta aproximación es generar siempre conocimiento validado sobre un producto o solución, tanto para preparar el lanzamiento o una vez en el mercado.Para lograr este objetivo, el Lean UX Canvas pone en el centro el problema que hay que resolver (en lugar de una solución a implementar) y define los aspectos necesarios para construir, medir y aprender.
     </p>
          
     <p>Ventajas</p>
